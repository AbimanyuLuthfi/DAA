#Program Penjumlahan Pada Python

def getSum(myList):
    sum = 0
    for item in myList:
        sum = sum+item
    return sum
    
getSum([1,2,3,4])

#Program Perkalian Pada Python

def getKali(myList):
    kali = 1
    for item in myList:
        kali = kali*item
    return kali
    
getKali([1,2,3,4,5])

#Program Pembagian Pada Python

def getBagi(myList):
    bagi = 100
    for item in myList:
        bagi = bagi/item
    return bagi
    
getBagi([5,10])
